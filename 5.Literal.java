class Literals{
    public static void main(String args[]){
        
        int a = 45;
        System.out.println(a);

        a = 0b101;
        System.out.println(a);

        a = 0x7e;
        System.out.println(a);

        a = 1_111_00_00;
        System.out.println(a);

        char c = 'a';
        System.out.println(++c);


    }
}