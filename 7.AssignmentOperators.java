class AssignmentOperators{
    public static void main(String args[]){
        int a = 10;
        int b = 5;
        
        int c = a+b;
        System.out.println(c);

        c = a-b;
        System.out.println(c);

        c = a++;
        System.out.println(c);

        c = ++a;
        System.out.println(c);
    }
}